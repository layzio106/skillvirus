package lecture;

public class DataType {

	public static void main(String[] args) {
		char charVal = 'A'; //문자
		boolean booleanVal = true; //boolean
		byte byteVal = 50; //정수(-126~127)
		short shortVal = 190; //정수(-32,768~32,767)
		int intVal = 10; //정수(-2,147,483,638~2,147,483,647)
		long longVal = 10000000; //정수
		float floatVal = 10.5f; //실수
		double doubleVal = 10.5; //실수
		
		System.out.println("char 자료형 : " + charVal);
		System.out.println("boolean 자료형 : " + booleanVal);
		System.out.println("byte 자료형 : " + byteVal);
		System.out.println("short 자료형 : " + shortVal);
		System.out.println("int 자료형 : " + intVal);
		System.out.println("long 자료형 : " + longVal);
		System.out.println("float 자료형 : " + floatVal);
		System.out.println("double 자료형 : " + doubleVal);
	}

}
